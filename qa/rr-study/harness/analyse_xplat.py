        f"| Field | Value |",
        f"|---|---|",
        f"| Run date | {run_date_str} |",
        f"| OpenWSFZ git SHA | `{git_sha}` |",
        f"| Shim version (both platforms) | *(verify via GET /api/v1/status)* |",
        f"| Windows platform | win-x64, WASAPI, `libft8.dll` shim 20260030 |",
        f"| Linux platform | WSL2 linux-x64, ALSA-PulseAudio, `libft8.so` shim 20260030 |",
