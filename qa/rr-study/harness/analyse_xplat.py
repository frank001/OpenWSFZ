# Verdict collection (extended for xplat gates)
# ---------------------------------------------------------------------------

def _collect_verdicts(
    continuous_results: dict,
    kappa_results: dict,
    fp_results: dict,
    bias_results: dict,
    platform_bias: dict,
    mcnemar: dict,
    fisher_fp: dict,
) -> tuple[list[tuple], str, list[str]]:
    verdict_rows: list[tuple] = []
    fails: list[str] = []
    marginals: list[str] = []

    # Continuous GR&R
    for scen_id, res in continuous_results.items():
        if res is None:
            continue
        pct_grr = res["metrics"]["pct_contribution_grr"]
        ndc = res["metrics"]["ndc"]
        v_grr = _verdict_grr(pct_grr)
        v_ndc = _verdict_ndc(ndc)
        verdict_rows.append(("%GR&R", scen_id, f"{pct_grr:.1f}%", v_grr))
        verdict_rows.append(("ndc", scen_id, str(ndc), v_ndc))
        if v_grr == "FAIL":
            fails.append(f"%GR&R ({scen_id}) = {pct_grr:.1f}% (threshold < {THRESH_GRR_PASS}%)")
        elif v_grr == "MARGINAL":
            marginals.append(f"%GR&R ({scen_id}) = {pct_grr:.1f}%")

    # Platform bias gates (STUDY-SPEC-XPLAT §8.1)
    bias_gates = {
        "S1": ("SNR bias |Linux-Windows|", "dB",
               THRESH_PLATFORM_SNR_PASS, THRESH_PLATFORM_SNR_MARGINAL),
        "S2": ("Freq bias |Linux-Windows|", "Hz",
               THRESH_PLATFORM_FREQ_PASS, THRESH_PLATFORM_FREQ_MARGINAL),
        "S3": ("DT bias |Linux-Windows|", "s",
               THRESH_PLATFORM_DT_PASS, THRESH_PLATFORM_DT_MARGINAL),
    }
    for scen_id, (label, unit, pass_t, marg_t) in bias_gates.items():
        if scen_id not in platform_bias:
            continue
        b = platform_bias[scen_id].get("mean_bias", float("nan"))
        if math.isnan(b):
            continue
        v = _verdict_platform_bias(b, pass_t, marg_t)
        verdict_rows.append((label, scen_id, f"{b:+.3f} {unit}", v))
        if v == "FAIL":
            fails.append(f"{label} ({scen_id}) = {b:+.3f} {unit} (gate: <= {marg_t} {unit})")
        elif v == "MARGINAL":
            marginals.append(f"{label} ({scen_id}) = {b:+.3f} {unit}")

    # Between-platform Kappa
    for label, info in kappa_results.items():
        k = info.get("kappa", float("nan"))
        if math.isnan(k):
            continue
        if label == "between_platforms":
            v = _verdict_kappa_xplat(k)
            verdict_rows.append(("Between-platform kappa", "S4+S5", f"{k:.3f}", v))
            if v == "FAIL":
                fails.append(f"Between-platform kappa = {k:.3f} (gate >= {THRESH_KAPPA_XPLAT_PASS})")
            elif v == "MARGINAL":
                marginals.append(f"Between-platform kappa = {k:.3f}")
        else:
            v = _verdict_kappa(k)
            verdict_rows.append((f"Kappa {label}", "S4+S5", f"{k:.3f}", v))
            if v == "FAIL":
                fails.append(f"Kappa ({label}) = {k:.3f}")

    # FP event count
    for appr, info in fp_results.items():
        n_events = info.get("n_fp_events", float("nan"))
        if isinstance(n_events, float) and math.isnan(n_events):
            continue
        event_rate = info["event_rate"]
        rob_95 = info["rob_95"]
        rob_str = f"; 95% UB <= {rob_95:.2f}%" if rob_95 is not None else ""
        v = _verdict_fp(info)
        value_str = (f"{int(n_events)}/{info['n_slots']} slots "
                     f"(event {event_rate:.1f}%{rob_str})")
        verdict_rows.append(("FP event count", f"S5/{appr}", value_str, v))
        if v == "FAIL":
            fails.append(f"FP event count ({appr}): {int(n_events)} events (gate = 0)")

    # SNR bias per appraiser (STUDY-SPEC §8.1)
    for appr, info in bias_results.items():
        b = info.get("mean_bias", float("nan"))
        if math.isnan(b):
            continue
        v = _verdict_bias(b)
        verdict_rows.append(("SNR bias vs truth", f"S1/{appr}", f"{b:+.2f} dB", v))
        if v == "FAIL":
            fails.append(f"SNR bias ({appr}) = {b:+.2f} dB (gate <= {THRESH_BIAS_PASS} dB)")

    # McNemar
    p_mc = mcnemar.get("p_value", float("nan"))
    if not math.isnan(p_mc):
        v = mcnemar.get("verdict", "—")
        verdict_rows.append(("McNemar chi2 p-value", "all", f"p={p_mc:.4f}", v))
        if v == "FAIL":
            fails.append(f"McNemar test rejected at p={p_mc:.4f} (alpha={THRESH_MCNEMAR_P})")

    # Fisher FP parity
    p_fisher = fisher_fp.get("p_value", float("nan"))
    if not math.isnan(p_fisher):
        v = fisher_fp.get("verdict", "—")
        verdict_rows.append(("FP parity Fisher p", "S5", f"p={p_fisher:.4f}", v))
        if v == "FAIL":
            fails.append(f"FP parity Fisher test rejected at p={p_fisher:.4f}")

    overall = "FAIL" if fails else ("MARGINAL" if marginals else "PASS")
    return verdict_rows, overall, fails


