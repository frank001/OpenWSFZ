def _bias_linearity(df_matched: pd.DataFrame, run_dir: Path) -> dict:
    df = df_matched[df_matched["matched"] == True].copy()
    df["reported_snr_db"] = pd.to_numeric(df["reported_snr_db"], errors="coerce")
    df["true_snr_db"] = pd.to_numeric(df["true_snr_db"], errors="coerce")
    df = df.dropna(subset=["reported_snr_db", "true_snr_db"])
    df["bias"] = df["reported_snr_db"] - df["true_snr_db"]

    results: dict[str, dict] = {}

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.axhline(0, color="black", linestyle="--", lw=0.8)

    appraisers_in_data = [a for a in APPRAISERS if a in df["appraiser"].values]
    for appr in appraisers_in_data:
        sub = df[df["appraiser"] == appr]
        if sub.empty:
            continue
        x = sub["true_snr_db"].values
        y = sub["bias"].values
        mean_bias = float(np.mean(y))
        coeffs = np.polyfit(x, y, 1)
        slope, intercept = float(coeffs[0]), float(coeffs[1])
        y_pred = np.polyval(coeffs, x)
        ss_res = float(np.sum((y - y_pred) ** 2))
        ss_tot = float(np.sum((y - np.mean(y)) ** 2))
        r2 = 1.0 - ss_res / ss_tot if ss_tot > 0 else 0.0

        results[appr] = {"mean_bias": mean_bias, "slope": slope,
                         "intercept": intercept, "r2": r2}

        x_line = np.linspace(x.min(), x.max(), 50)
        ax.scatter(x, y, label=f"{appr} (bias)", alpha=0.6, s=20)
        ax.plot(x_line, np.polyval(coeffs, x_line),
                label=f"{appr} fit (slope={slope:.3f})")

    ax.set_xlabel("True SNR (dB)")
    ax.set_ylabel("Bias = Reported - True (dB)")
    ax.set_title("S1 Bias & Linearity")
    ax.legend(fontsize=8)
    plt.tight_layout()
    out_path = run_dir / "S1_bias_linearity.png"
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    return results


# ---------------------------------------------------------------------------
# Platform bias metrics — all N*(N-1)/2 appraiser pairs
# ---------------------------------------------------------------------------

def _platform_bias_metrics_all(
    df_matched: pd.DataFrame,
    response: str,
    appraisers: tuple[str, ...] = APPRAISERS,
) -> dict[tuple[str, str], dict]:
    """Compute pairwise (B − A) bias for every appraiser combination.

    Returns ``{(A, B): bias_dict}`` for all i<j pairs in *appraisers*.
    ``bias = appraiser_B − appraiser_A`` (same sign convention as the
    original Linux−Windows analysis).  Pairs where either appraiser has no
    data return NaN metrics — the caller must handle these gracefully.
    """
    df = df_matched[df_matched["matched"] == True].copy()
    df[response] = pd.to_numeric(df[response], errors="coerce")
    df = df.dropna(subset=[response, "part_index", "trial_index", "appraiser"])

    pivot = df.pivot_table(
        index=["part_index", "trial_index"],
        columns="appraiser",
        values=response,
        aggfunc="mean",
    )

    _nan_row: dict = {"mean_bias": float("nan"), "t_stat": float("nan"),
                      "p_value": float("nan"), "n_pairs": 0, "verdict": "—"}
    results: dict[tuple[str, str], dict] = {}

    for a, b in itertools.combinations(appraisers, 2):
        if a not in pivot.columns or b not in pivot.columns:
            results[(a, b)] = dict(_nan_row)
            continue
        paired = pivot[[a, b]].dropna()
        n = len(paired)
        if n < 2:
            results[(a, b)] = {**_nan_row, "n_pairs": n}
            continue
        diffs = paired[b] - paired[a]
        mean_bias = float(diffs.mean())
        se = float(diffs.std(ddof=1) / np.sqrt(n))
        t_stat = mean_bias / se if se > 0 else float("inf")
        p_value = float(2 * (1 - tdist.cdf(abs(t_stat), df=n - 1)))
        results[(a, b)] = {
            "mean_bias": mean_bias, "t_stat": t_stat,
            "p_value": p_value, "n_pairs": n, "verdict": "—",
        }
    return results


# ---------------------------------------------------------------------------
# Platform bias chart — multi-panel, one panel per appraiser pair
# ---------------------------------------------------------------------------

def _platform_bias_chart(
    df_s1: pd.DataFrame,
    run_dir: Path,
    appraisers: tuple[str, ...] = APPRAISERS,
) -> Path | None:
    """Scatter plots of (SNR_B − SNR_A) vs true SNR for every appraiser pair.

    Generates a single PNG with one subplot per pair.  Returns None if no
    pair has sufficient data.
    """
    pairs = list(itertools.combinations(appraisers, 2))
    if not pairs:
        return None

    df = df_s1[df_s1["matched"] == True].copy()
    df["reported_snr_db"] = pd.to_numeric(df["reported_snr_db"], errors="coerce")
    df["true_snr_db"] = pd.to_numeric(df["true_snr_db"], errors="coerce")
    df = df.dropna(subset=["reported_snr_db", "true_snr_db", "part_index", "trial_index"])

    pivot = df.pivot_table(
        index=["part_index", "trial_index"],
        columns="appraiser",
        values="reported_snr_db",
        aggfunc="mean",
    )
    true_snr = df.groupby(["part_index", "trial_index"])["true_snr_db"].mean()
    pivot = pivot.join(true_snr, how="inner")

    n_pairs = len(pairs)
    fig, axes = plt.subplots(1, n_pairs, figsize=(7 * n_pairs, 5), squeeze=False)
    any_data = False

    for idx, (a, b) in enumerate(pairs):
        ax = axes[0][idx]
        if a not in pivot.columns or b not in pivot.columns:
            ax.text(0.5, 0.5, f"No data\n{b} − {a}",
                    transform=ax.transAxes, ha="center", va="center")
            ax.set_title(f"S1 SNR Bias ({b} − {a})")
            continue
        paired = pivot[[a, b, "true_snr_db"]].dropna()
        if len(paired) < 2:
            continue
        diffs = (paired[b] - paired[a]).values
        x = paired["true_snr_db"].values
        coeffs = np.polyfit(x, diffs, 1)
        x_line = np.linspace(x.min(), x.max(), 50)

        ax.axhline(0, color="black", linestyle="--", lw=0.8, label="Zero bias")
        ax.axhline( 2, color="red", linestyle=":", lw=0.8, label="+2 dB gate")
        ax.axhline(-2, color="red", linestyle=":", lw=0.8, label="-2 dB gate")
        ax.scatter(x, diffs, alpha=0.7, s=25, label=f"{b}−{a} (n={len(diffs)})")
        ax.plot(x_line, np.polyval(coeffs, x_line), color="orange",
                label=f"Fit: slope={coeffs[0]:.3f}")
        ax.set_xlabel("True SNR (dB)")
        ax.set_ylabel(f"SNR_{b} − SNR_{a} (dB)")
        ax.set_title(f"S1 Platform SNR Bias ({b} − {a})")
        ax.legend(fontsize=7)
        any_data = True

    if not any_data:
        plt.close(fig)
        return None

    plt.tight_layout()
    out_path = run_dir / "S1_platform_bias.png"
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    return out_path


