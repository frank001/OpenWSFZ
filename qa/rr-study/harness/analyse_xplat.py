def _platform_bias_metrics(df_matched: pd.DataFrame, response: str) -> dict:
    """Compute Linux − Windows bias for a continuous response variable.

    Requires both appraisers to have decoded the same (part, trial) cell.
    Returns dict with mean_bias, t_stat, p_value, n_pairs, and verdict.
    """
    df = df_matched[df_matched["matched"] == True].copy()
    df[response] = pd.to_numeric(df[response], errors="coerce")
    df = df.dropna(subset=[response, "part_index", "trial_index", "appraiser"])

    # Pivot to wide: one row per (part, trial), columns Windows / Linux
    pivot = df.pivot_table(
        index=["part_index", "trial_index"],
        columns="appraiser",
        values=response,
        aggfunc="mean",
    )
    if "Windows" not in pivot.columns or "Linux" not in pivot.columns:
        return {"mean_bias": float("nan"), "t_stat": float("nan"),
                "p_value": float("nan"), "n_pairs": 0, "verdict": "—"}

    paired = pivot[["Windows", "Linux"]].dropna()
    n = len(paired)
    if n < 2:
        return {"mean_bias": float("nan"), "t_stat": float("nan"),
                "p_value": float("nan"), "n_pairs": n, "verdict": "—"}

    diffs = paired["Linux"] - paired["Windows"]
    mean_bias = float(diffs.mean())
    se = float(diffs.std(ddof=1) / np.sqrt(n))
    t_stat = mean_bias / se if se > 0 else float("inf")
    p_value = float(2 * (1 - tdist.cdf(abs(t_stat), df=n - 1)))

    return {
        "mean_bias": mean_bias,
        "t_stat": t_stat,
        "p_value": p_value,
        "n_pairs": n,
        "verdict": "—",  # caller applies gate
    }


def _platform_bias_chart(df_s1: pd.DataFrame, run_dir: Path) -> Path | None:
    """Scatter plot of (SNR_Linux - SNR_Windows) vs true SNR with 95% CI band."""
    df = df_s1[df_s1["matched"] == True].copy()
    df["reported_snr_db"] = pd.to_numeric(df["reported_snr_db"], errors="coerce")
    df["true_snr_db"] = pd.to_numeric(df["true_snr_db"], errors="coerce")
    df = df.dropna(subset=["reported_snr_db", "true_snr_db", "part_index", "trial_index"])

    pivot = df.pivot_table(
        index=["part_index", "trial_index"],
        columns="appraiser",
        values="reported_snr_db",
        aggfunc="mean",
    )
    if "Windows" not in pivot.columns or "Linux" not in pivot.columns:
        return None

    paired = pivot[["Windows", "Linux"]].dropna()
    if len(paired) < 2:
        return None

    # Merge true_snr_db
    true_snr = df.groupby(["part_index", "trial_index"])["true_snr_db"].mean()
    paired = paired.join(true_snr, how="inner")
    diffs = paired["Linux"] - paired["Windows"]
    x = paired["true_snr_db"].values
    y = diffs.values

    coeffs = np.polyfit(x, y, 1)
    x_line = np.linspace(x.min(), x.max(), 50)

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.axhline(0, color="black", linestyle="--", lw=0.8, label="Zero bias")
    ax.axhline(2, color="red", linestyle=":", lw=0.8, label="+2 dB gate")
    ax.axhline(-2, color="red", linestyle=":", lw=0.8, label="-2 dB gate")
    ax.scatter(x, y, alpha=0.7, s=25, label=f"Linux-Windows (n={len(y)})")
    ax.plot(x_line, np.polyval(coeffs, x_line), color="orange",
            label=f"Fit: slope={coeffs[0]:.3f}")
    ax.set_xlabel("True SNR (dB)")
    ax.set_ylabel("SNR_Linux - SNR_Windows (dB)")
    ax.set_title("S1 Platform SNR Bias (Linux - Windows)")
    ax.legend(fontsize=8)
    plt.tight_layout()
    out_path = run_dir / "S1_platform_bias.png"
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    return out_path


def _dt_jitter_timeseries(df_s3: pd.DataFrame, run_dir: Path) -> Path | None:
    """Time-series of DT_Linux - DT_Windows across all S3 trials (in cycle order)."""
    df = df_s3[df_s3["matched"] == True].copy()
    df["reported_dt_s"] = pd.to_numeric(df["reported_dt_s"], errors="coerce")
    df = df.dropna(subset=["reported_dt_s", "part_index", "trial_index",
                            "appraiser", "cycle_utc"])

    pivot = df.pivot_table(
        index=["cycle_utc", "part_index", "trial_index"],
        columns="appraiser",
        values="reported_dt_s",
        aggfunc="mean",
    ).reset_index()

    if "Windows" not in pivot.columns or "Linux" not in pivot.columns:
        return None

    paired = pivot[["cycle_utc", "part_index", "trial_index",
                    "Windows", "Linux"]].dropna()
    if len(paired) < 2:
        return None

    paired = paired.sort_values("cycle_utc")
    diffs = (paired["Linux"] - paired["Windows"]).values
    x = np.arange(len(diffs))

    fig, ax = plt.subplots(figsize=(10, 4))
    ax.axhline(0, color="black", linestyle="--", lw=0.8)
    ax.axhline(0.1, color="orange", linestyle=":", lw=0.8, label="±0.1 s PASS gate")
    ax.axhline(-0.1, color="orange", linestyle=":", lw=0.8)
    ax.axhline(0.2, color="red", linestyle=":", lw=0.8, label="±0.2 s MARGINAL gate")
    ax.axhline(-0.2, color="red", linestyle=":", lw=0.8)
    ax.plot(x, diffs, marker="o", ms=4, label="DT_Linux - DT_Windows")
    # Rolling mean (window 5)
    if len(diffs) >= 5:
        rm = pd.Series(diffs).rolling(5, center=True).mean()
        ax.plot(x, rm, color="navy", lw=1.5, label="Rolling mean (w=5)")
    ax.set_xlabel("Trial (cycle order)")
    ax.set_ylabel("DT_Linux - DT_Windows (s)")
    ax.set_title("S3 DT Jitter Time-Series (Linux - Windows)")
    ax.legend(fontsize=8)
    plt.tight_layout()
    out_path = run_dir / "S3_dt_jitter.png"
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    return out_path


