_TREND_COLUMNS_XPLAT = [
    "run_date", "git_sha",
    "pct_grr_snr", "ndc_snr",
    "platform_snr_bias_db",
    "between_platform_kappa",
    "fp_events_windows", "fp_events_linux",
    "mcnemar_p", "overall",
]


def _append_trend(qa_rr_root: Path, run_dir: Path, git_sha: str,
                  continuous_results: dict, kappa_results: dict,
                  fp_results: dict, platform_bias: dict,
                  mcnemar: dict, overall: str) -> None:
    import csv as _csv
    trend_path = qa_rr_root / "trend_xplat.csv"
    write_header = not trend_path.exists()

    run_date_parts = run_dir.name.split("-")[0:3]
    run_date_str = "-".join(run_date_parts) if len(run_date_parts) >= 3 else run_dir.name

    def _safe(v: Any) -> str:
        if v is None or (isinstance(v, float) and math.isnan(v)):
            return ""
        return str(round(v, 4) if isinstance(v, float) else v)

    pct_grr_snr = ndc_snr = ""
    if "S1" in continuous_results and continuous_results["S1"] is not None:
        pct_grr_snr = _safe(continuous_results["S1"]["metrics"]["pct_contribution_grr"])
        ndc_snr = _safe(continuous_results["S1"]["metrics"]["ndc"])

    platform_snr_bias_db = _safe(platform_bias.get("S1", {}).get("mean_bias", float("nan")))

    bk = kappa_results.get("between_platforms", {})
    between_kappa = _safe(bk.get("kappa", float("nan")))

    win_fp = _safe(fp_results.get("Windows", {}).get("n_fp_events", float("nan")))
    lin_fp = _safe(fp_results.get("Linux", {}).get("n_fp_events", float("nan")))
    mc_p = _safe(mcnemar.get("p_value", float("nan")))

    row = {
        "run_date": run_date_str,
        "git_sha": git_sha,
        "pct_grr_snr": pct_grr_snr,
        "ndc_snr": ndc_snr,
        "platform_snr_bias_db": platform_snr_bias_db,
        "between_platform_kappa": between_kappa,
        "fp_events_windows": win_fp,
        "fp_events_linux": lin_fp,
        "mcnemar_p": mc_p,
        "overall": overall,
    }

    with open(trend_path, "a", newline="", encoding="utf-8") as fh:
        writer = _csv.DictWriter(fh, fieldnames=_TREND_COLUMNS_XPLAT)
        if write_header:
            writer.writeheader()
        writer.writerow(row)


# ---------------------------------------------------------------------------
# Main entry point
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Cross-platform R&R analyser (Windows vs Linux/WSL2)"
    )
    parser.add_argument("--run-dir", required=True,
                        help="Run directory containing *_matched.csv files")
    parser.add_argument("--scenario",
                        help="Comma-separated scenario IDs to analyse (default: all found)")
    args = parser.parse_args()

    run_dir = Path(args.run_dir)
    if not run_dir.exists():
        sys.exit(f"ERROR: run directory does not exist: {run_dir}")

    scenario_filter = [s.strip() for s in args.scenario.split(",")] if args.scenario else None
    scenarios_dir = _QA_ROOT / "scenarios"

    matched = _load_matched_csvs(run_dir, scenario_filter, scenarios_dir)
    if not matched:
        sys.exit(f"ERROR: no *_matched.csv files found in {run_dir}")

    print(f"Analysing: {', '.join(sorted(matched.keys()))}")
    git_sha = _git_sha()
    scenario_meta = _load_scenario_meta(scenarios_dir)

    continuous_results: dict = {}
    kappa_results: dict = {}
    fp_results: dict = {}
    bias_results: dict = {}
    platform_bias: dict = {}

    # --- Continuous GR&R (S1, S2, S3) ---
    for scen_id, df in matched.items():
        if scen_id not in CONTINUOUS_SCENARIOS:
            continue
        response = RESPONSE_VAR[scen_id]
        tol_half = TOLERANCE_HALF[scen_id]

        # NOTE: No WSJT-X DT correction in xplat study — both platforms use UTC convention.

        anova = _two_way_anova(df, response, scen_id)
        if anova is None:
            continuous_results[scen_id] = None
            continue
        metrics = _derived_metrics(anova, tol_half)
        continuous_results[scen_id] = {
            "anova": anova, "metrics": metrics,
            "response": response, "tol_half": tol_half,
        }
        print(f"  {scen_id}: %GR&R={metrics['pct_contribution_grr']:.1f}%  ndc={metrics['ndc']}")
        png = _plot_grr_panel(df, response, anova, metrics, scen_id, run_dir)
        print(f"    -> {png.name}")

        # Platform bias (Linux - Windows)
        pb = _platform_bias_metrics(df, response)
        platform_bias[scen_id] = pb
        print(f"  {scen_id} platform bias (Linux-Windows): {_fmt_num(pb['mean_bias'], '+.3f')} "
              f"{response.split('_')[1]}  p={_fmt_num(pb['p_value'], '.4f')}  "
              f"n={pb['n_pairs']}")

    # --- S1 Bias & Linearity ---
    if "S1" in matched and continuous_results.get("S1") is not None:
        bias_results = _bias_linearity(matched["S1"], run_dir)
        for appr, info in bias_results.items():
            print(f"  S1 bias ({appr}): {info['mean_bias']:+.2f} dB  slope={info['slope']:.3f}")
        # Platform bias scatter chart
        chart = _platform_bias_chart(matched["S1"], run_dir)
        if chart:
            print(f"    -> {chart.name}")

    # --- S3 DT jitter time-series ---
    if "S3" in matched and continuous_results.get("S3") is not None:
        jitter_chart = _dt_jitter_timeseries(matched["S3"], run_dir)
        if jitter_chart:
            print(f"  S3 DT jitter time-series -> {jitter_chart.name}")

    # --- Decode-rate companions (S1b, S3b) ---
    decode_rate_results: list[dict] = []
    for sid in sorted(DECODE_RATE_SCENARIOS):
        if sid not in matched:
            continue
        cfg = DECODE_RATE_CONFIG[sid]
        dr = _analyse_decode_rate(
            matched[sid], sid, run_dir,
            part_var=cfg["part_var"],
            part_label=cfg["part_label"],
        )
        decode_rate_results.append(dr)
        for appr in APPRAISERS:
            rate = dr["overall"].get(appr, float("nan"))
            print(f"  {sid} decode rate ({appr}): {_fmt_num(rate)}%  (informational)")

    # --- Attribute (S4 + S5) ---
    attr_results: dict | None = None
    if "S4" in matched or "S5" in matched:
        attr_results = _attribute_agreement(matched, run_dir)
        kappa_results = attr_results["kappa"]
        for label, info in kappa_results.items():
            print(f"  Kappa {label}: {_fmt_num(info.get('kappa', float('nan')), '.3f')}")

    # --- FP rate (S5) ---
    if "S5" in matched:
        fp_results = _fp_rate(matched["S5"])
        for appr, info in fp_results.items():
            n_events = info.get("n_fp_events", float("nan"))
            if isinstance(n_events, float) and math.isnan(n_events):
                print(f"  S5 FP ({appr}): undefined")
                continue
            print(
                f"  S5 FP ({appr}): {int(n_events)}/{info['n_slots']} events "
                f"(event rate {_fmt_num(info['event_rate'])}%)"
            )

    # --- McNemar's test (pooled all scenarios) ---
    mcnemar = _mcnemar_test(matched)
    print(f"  McNemar: n12={mcnemar['n12']} n21={mcnemar['n21']} "
          f"chi2={_fmt_num(mcnemar['chi2'], '.3f')} p={_fmt_num(mcnemar['p_value'], '.4f')} "
          f"-> {mcnemar['verdict']}")

    # --- Fisher FP parity ---
    fisher_fp = _fp_parity_fisher(fp_results) if fp_results else {"p_value": float("nan"), "verdict": "—"}
    if not math.isnan(fisher_fp["p_value"]):
        print(f"  Fisher FP parity: p={_fmt_num(fisher_fp['p_value'], '.4f')} -> {fisher_fp['verdict']}")

    # --- S7 co-channel ---
    s7_results: dict | None = None
    if "S7" in matched:
        s7_results = _analyse_compounding(matched["S7"], scenario_meta.get("S7"), run_dir)
        for appr in APPRAISERS:
            rate = s7_results["overall"].get(appr, float("nan"))
            print(f"  S7 recovery ({appr}): {_fmt_num(rate)}%")

    # --- Verdicts ---
    verdict_rows, overall, fails = _collect_verdicts(
        continuous_results, kappa_results, fp_results, bias_results,
        platform_bias, mcnemar, fisher_fp,
    )

    # --- Report ---
    report_path = _write_report(
        run_dir, git_sha, continuous_results, kappa_results,
        fp_results, bias_results, platform_bias, mcnemar, fisher_fp,
        verdict_rows, overall, fails,
        attr_results=attr_results,
        s7_results=s7_results,
        decode_rate_results=decode_rate_results,
    )
    print(f"\nReport written: {report_path}")
    print(f"Overall verdict: {overall}")

    if fails:
        print("\n[!] DEFECT NOTICES:")
        for f in fails:
            print(f"  FAIL — {f}")

    _append_trend(
        _QA_ROOT, run_dir, git_sha,
        continuous_results, kappa_results, fp_results,
        platform_bias, mcnemar, overall,
    )
    print(f"Trend row appended: {_QA_ROOT / 'trend_xplat.csv'}")
    print(f"\nRemember (HK-001): complete Sections 1 and 5 in {report_path}")


if __name__ == "__main__":
    main()
