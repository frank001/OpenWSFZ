def _dt_jitter_timeseries(
    df_s3: pd.DataFrame,
    run_dir: Path,
    appraisers: tuple[str, ...] = APPRAISERS,
) -> Path | None:
    """Time-series of DT_B − DT_A for all appraiser pairs across S3 trials."""
    pairs = list(itertools.combinations(appraisers, 2))
    if not pairs:
        return None

    df = df_s3[df_s3["matched"] == True].copy()
    df["reported_dt_s"] = pd.to_numeric(df["reported_dt_s"], errors="coerce")
    df = df.dropna(subset=["reported_dt_s", "part_index", "trial_index",
                            "appraiser", "cycle_utc"])

    pivot = df.pivot_table(
        index=["cycle_utc", "part_index", "trial_index"],
        columns="appraiser",
        values="reported_dt_s",
        aggfunc="mean",
    ).reset_index().sort_values("cycle_utc")

    fig, ax = plt.subplots(figsize=(10, 4))
    ax.axhline(0, color="black", linestyle="--", lw=0.8)
    ax.axhline( 0.1, color="orange", linestyle=":", lw=0.8, label="±0.1 s PASS gate")
    ax.axhline(-0.1, color="orange", linestyle=":", lw=0.8)
    ax.axhline( 0.2, color="red",    linestyle=":", lw=0.8, label="±0.2 s MARGINAL gate")
    ax.axhline(-0.2, color="red",    linestyle=":", lw=0.8)

    any_data = False
    for a, b in pairs:
        if a not in pivot.columns or b not in pivot.columns:
            continue
        paired = pivot[[a, b, "cycle_utc"]].dropna()
        if len(paired) < 2:
            continue
        diffs = (paired[b] - paired[a]).values
        x = np.arange(len(diffs))
        ax.plot(x, diffs, marker="o", ms=3, label=f"DT_{b} − DT_{a}")
        if len(diffs) >= 5:
            rm = pd.Series(diffs).rolling(5, center=True).mean()
            ax.plot(x, rm, lw=1.5, linestyle="--",
                    label=f"Rolling mean ({b}−{a})")
        any_data = True

    if not any_data:
        plt.close(fig)
        return None

    ax.set_xlabel("Trial (cycle order)")
    ax.set_ylabel("DT difference (s)")
    ax.set_title("S3 DT Jitter Time-Series (all pairs)")
    ax.legend(fontsize=8)
    plt.tight_layout()
    out_path = run_dir / "S3_dt_jitter.png"
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    return out_path


# ---------------------------------------------------------------------------
# McNemar's test — all appraiser pairs
# ---------------------------------------------------------------------------

def _mcnemar_test(
    df_all: dict[str, pd.DataFrame],
    appraisers: tuple[str, ...] = APPRAISERS,
) -> dict[tuple[str, str], dict]:
    """McNemar's test on pooled binary decode decisions for every appraiser pair.

    Returns ``{(A, B): result_dict}`` for all i<j pairs.
    *n_ab* = A decoded, B did not; *n_ba* = B decoded, A did not.
    """
    counts: dict[tuple[str, str], list[int]] = {
        (a, b): [0, 0] for a, b in itertools.combinations(appraisers, 2)
    }

    for scen_id, df in df_all.items():
        df_sub = df[df["false_positive"] == False].copy()
        if df_sub.empty:
            continue
        pivot = df_sub.pivot_table(
            index=["part_index", "trial_index", "message_text", "cycle_utc"],
            columns="appraiser",
            values="matched",
            aggfunc="any",
        )
        for (a, b), cnt in counts.items():
            if a not in pivot.columns or b not in pivot.columns:
                continue
            paired = pivot[[a, b]].dropna()
            cnt[0] += int(((paired[a] == True) & (paired[b] == False)).sum())
            cnt[1] += int(((paired[a] == False) & (paired[b] == True)).sum())

    results: dict[tuple[str, str], dict] = {}
    for (a, b), (n_ab, n_ba) in counts.items():
        total = n_ab + n_ba
        if total == 0:
            results[(a, b)] = {"n_ab": 0, "n_ba": 0, "chi2": float("nan"),
                               "p_value": float("nan"), "verdict": "PASS"}
            continue
        chi2 = (abs(n_ab - n_ba) - 1) ** 2 / total
        p_value = float(1 - chi2dist.cdf(chi2, df=1))
        results[(a, b)] = {
            "n_ab": n_ab, "n_ba": n_ba,
            "chi2": chi2, "p_value": p_value,
            "verdict": _verdict_mcnemar(p_value),
        }
    return results


