def _mcnemar_test(df_all: dict[str, pd.DataFrame]) -> dict:
    """McNemar's test on pooled binary decode decisions (all non-FP scenarios).

    Returns dict with n12 (Win+, Lin-), n21 (Win-, Lin+), chi2, p_value, verdict.
    """
    n12 = 0  # Windows decoded, Linux did not
    n21 = 0  # Linux decoded, Windows did not

    for scen_id, df in df_all.items():
        df_sub = df[df["false_positive"] == False].copy()
        if df_sub.empty:
            continue
        pivot = df_sub.pivot_table(
            index=["part_index", "trial_index", "message_text", "cycle_utc"],
            columns="appraiser",
            values="matched",
            aggfunc="any",
        )
        if "Windows" not in pivot.columns or "Linux" not in pivot.columns:
            continue
        paired = pivot[["Windows", "Linux"]].dropna()
        n12 += int(((paired["Windows"] == True) & (paired["Linux"] == False)).sum())
        n21 += int(((paired["Windows"] == False) & (paired["Linux"] == True)).sum())

    total = n12 + n21
    if total == 0:
        return {"n12": 0, "n21": 0, "chi2": float("nan"),
                "p_value": float("nan"), "verdict": "PASS"}

    # McNemar's with continuity correction
    chi2 = (abs(n12 - n21) - 1) ** 2 / total
    p_value = float(1 - chi2dist.cdf(chi2, df=1))
    return {
        "n12": n12, "n21": n21,
        "chi2": chi2, "p_value": p_value,
        "verdict": _verdict_mcnemar(p_value),
    }


