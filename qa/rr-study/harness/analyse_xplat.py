"""Cross-platform Gauge R&R analyser — Windows (WASAPI) vs Linux/WSL2 vs WSJT-X.

Usage:
    python harness/analyse_xplat.py --run-dir <dir> [--scenario S1,S2]

Reads *_matched.csv files generated by harness/matcher_xplat.py (appraiser
names "Windows", "Linux", and optionally "WSJT-X"), computes ANOVA-method
Gauge R&R variance components, cross-platform bias, McNemar's test, FP
parity, DT jitter, and writes report.md + PNG charts.

Three-appraiser operation:
  When wsjt-all.txt is present in the run directory, WSJT-X is included as
  a third appraiser and all pairwise metrics (bias, McNemar, Fisher, kappa)
  are computed for all N*(N-1)/2 = 3 pairs.

Graceful degradation:
  If WSJT-X rows are absent from the matched CSVs (because wsjt-all.txt was
  not available during matching), all WSJT-X-related pair metrics return NaN
  and the report omits the affected rows — no crash.

Key differences from harness/analyse.py:
  - Appraisers: "Windows" / "Linux" / "WSJT-X" (Windows+Linux only if WSJT-X absent)
  - No WSJT-X DT convention correction (all platforms use UTC-boundary DT)
  - Added: platform SNR/DT/freq bias gate for all pairs, DT jitter time-series,
    McNemar's test for all pairs, Fisher's exact FP parity for all pairs
  - report.md uses NFR-023 five-section structure (Sections 1 & 5 QA-authored)
"""
