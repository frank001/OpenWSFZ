def _fp_rate(df_matched: pd.DataFrame) -> dict[str, dict]:
    """FP rate per appraiser for S5 (scoped to S5 injection window)."""
    _NAN: dict = {
        "decode_rate": float("nan"), "event_rate": float("nan"),
        "n_fp_events": float("nan"), "n_fp_decodes": float("nan"),
        "n_slots": 0, "rob_95": None,
    }
    results: dict[str, dict] = {}

    s5_truth_rows = df_matched[df_matched["false_positive"] == False]
    s5_cycles: set = set(s5_truth_rows["cycle_utc"].dropna().unique())

    for appr in APPRAISERS:
        sub = df_matched[df_matched["appraiser"] == appr]
        truth_sub = sub[sub["false_positive"] == False]
        fp_sub = sub[sub["false_positive"] == True]
        n_slots = int(len(truth_sub))
        fp_in_window = fp_sub[fp_sub["cycle_utc"].isin(s5_cycles)] if s5_cycles else fp_sub

        if n_slots == 0:
            results[appr] = _NAN.copy()
            continue

        n_fp_decodes = int(len(fp_in_window))
        n_fp_events = int(fp_in_window["cycle_utc"].nunique())
        decode_rate = 100.0 * n_fp_decodes / n_slots
        event_rate = 100.0 * n_fp_events / n_slots
        rob_95 = (300.0 / n_slots) if n_fp_events == 0 else None

        results[appr] = {
            "decode_rate": decode_rate, "event_rate": event_rate,
            "n_fp_events": n_fp_events, "n_fp_decodes": n_fp_decodes,
            "n_slots": n_slots, "rob_95": rob_95,
        }
    return results


def _fp_parity_fisher(fp_results: dict) -> dict:
    """Fisher's exact test on FP count: Linux vs Windows."""
    win = fp_results.get("Windows", {})
    lin = fp_results.get("Linux", {})
    win_events = win.get("n_fp_events", float("nan"))
    lin_events = lin.get("n_fp_events", float("nan"))
    win_slots = win.get("n_slots", 0)
    lin_slots = lin.get("n_slots", 0)

    if (isinstance(win_events, float) and math.isnan(win_events)) or win_slots == 0 or lin_slots == 0:
        return {"p_value": float("nan"), "verdict": "—"}

    # 2x2 table: rows=FP/noFP, cols=Windows/Linux
    table = [[int(win_events), int(lin_events)],
             [win_slots - int(win_events), lin_slots - int(lin_events)]]
    try:
        _, p = fisher_exact(table)
        return {"p_value": float(p), "verdict": _verdict_fisher(p)}
    except Exception:
        return {"p_value": float("nan"), "verdict": "—"}


