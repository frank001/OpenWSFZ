# STUDY-SPEC-XPLAT §8.1 — platform bias gates
THRESH_PLATFORM_SNR_PASS      = 1.0    # dB — PASS
THRESH_PLATFORM_SNR_MARGINAL  = 2.0    # dB — MARGINAL; > 2 dB → FAIL
THRESH_PLATFORM_DT_PASS       = 0.1    # s
THRESH_PLATFORM_DT_MARGINAL   = 0.2    # s
