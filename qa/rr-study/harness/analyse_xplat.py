def _verdict_platform_bias(bias: float, pass_thr: float, marginal_thr: float) -> str:
    if abs(bias) <= pass_thr:
        return "PASS"
    if abs(bias) <= marginal_thr:
        return "MARGINAL"
    return "FAIL"


def _verdict_kappa_xplat(kappa: float) -> str:
    if kappa >= THRESH_KAPPA_XPLAT_PASS:
        return "PASS"
    if kappa >= THRESH_KAPPA_XPLAT_MARGINAL:
        return "MARGINAL"
    return "FAIL"


def _verdict_mcnemar(p: float) -> str:
    return "PASS" if p >= THRESH_MCNEMAR_P else "FAIL"


def _verdict_fisher(p: float) -> str:
    return "PASS" if p >= THRESH_MCNEMAR_P else "FAIL"


