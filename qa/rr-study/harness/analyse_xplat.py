# Verdict collection (extended for all pairs)
# ---------------------------------------------------------------------------

def _collect_verdicts(
    continuous_results: dict,
    kappa_results: dict,
    fp_results: dict,
    bias_results: dict,
    platform_bias: dict,
    mcnemar: dict,
    fisher_fp: dict,
) -> tuple[list[tuple], str, list[str]]:
    """Return (verdict_rows, overall_verdict, fail_messages).

    *platform_bias* : ``{scen_id: {(A, B): bias_dict}}``
    *mcnemar*       : ``{(A, B): mcnemar_dict}``
    *fisher_fp*     : ``{(A, B): fisher_dict}``
    """
    verdict_rows: list[tuple] = []
    fails:     list[str] = []
    marginals: list[str] = []

    # ── Continuous GR&R ──────────────────────────────────────────────────────
    for scen_id, res in continuous_results.items():
        if res is None:
            continue
        pct_grr = res["metrics"]["pct_contribution_grr"]
        ndc     = res["metrics"]["ndc"]
        v_grr   = _verdict_grr(pct_grr)
        v_ndc   = _verdict_ndc(ndc)
        verdict_rows.append(("%GR&R",  scen_id, f"{pct_grr:.1f}%", v_grr))
        verdict_rows.append(("ndc",    scen_id, str(ndc),           v_ndc))
        if v_grr == "FAIL":
            fails.append(f"%GR&R ({scen_id}) = {pct_grr:.1f}% (threshold < {THRESH_GRR_PASS}%)")
        elif v_grr == "MARGINAL":
            marginals.append(f"%GR&R ({scen_id}) = {pct_grr:.1f}%")

    # ── Platform bias gates — all pairs (STUDY-SPEC-XPLAT §8.1) ─────────────
    scen_bias_config = {
        "S1": ("SNR bias", "dB",
               THRESH_PLATFORM_SNR_PASS, THRESH_PLATFORM_SNR_MARGINAL),
        "S2": ("Freq bias", "Hz",
               THRESH_PLATFORM_FREQ_PASS, THRESH_PLATFORM_FREQ_MARGINAL),
        "S3": ("DT bias",  "s",
               THRESH_PLATFORM_DT_PASS,  THRESH_PLATFORM_DT_MARGINAL),
    }
    for scen_id, (label_base, unit, pass_t, marg_t) in scen_bias_config.items():
        if scen_id not in platform_bias:
            continue
        for (a, b), pb in platform_bias[scen_id].items():
            b_val = pb.get("mean_bias", float("nan"))
            if math.isnan(b_val):
                continue
            label = f"{label_base} |{b}−{a}|"
            v = _verdict_platform_bias(b_val, pass_t, marg_t)
            verdict_rows.append((label, scen_id, f"{b_val:+.3f} {unit}", v))
            if v == "FAIL":
                fails.append(
                    f"{label} ({scen_id}) = {b_val:+.3f} {unit} (gate: <= {marg_t} {unit})"
                )
            elif v == "MARGINAL":
                marginals.append(f"{label} ({scen_id}) = {b_val:+.3f} {unit}")

    # ── Kappa ────────────────────────────────────────────────────────────────
    for label, info in kappa_results.items():
        k = info.get("kappa", float("nan"))
        if math.isnan(k):
            continue
        if "_vs_truth" in label:
            v = _verdict_kappa(k)
            verdict_rows.append((f"Kappa {label}", "S4+S5", f"{k:.3f}", v))
            if v == "FAIL":
                fails.append(f"Kappa ({label}) = {k:.3f}")
        else:
            # Between-appraiser kappa
            v = _verdict_kappa_xplat(k)
            verdict_rows.append((f"Between-appraiser kappa {label}", "S4+S5", f"{k:.3f}", v))
            if v == "FAIL":
                fails.append(
                    f"Between-appraiser kappa ({label}) = {k:.3f} "
                    f"(gate >= {THRESH_KAPPA_XPLAT_PASS})"
                )
            elif v == "MARGINAL":
                marginals.append(f"Between-appraiser kappa ({label}) = {k:.3f}")

    # ── FP event count ───────────────────────────────────────────────────────
    for appr, info in fp_results.items():
        n_events = info.get("n_fp_events", float("nan"))
        if isinstance(n_events, float) and math.isnan(n_events):
            continue
        event_rate = info["event_rate"]
        rob_95     = info["rob_95"]
        rob_str    = f"; 95% UB <= {rob_95:.2f}%" if rob_95 is not None else ""
        v          = _verdict_fp(info)
        value_str  = (
            f"{int(n_events)}/{info['n_slots']} slots "
            f"(event {event_rate:.1f}%{rob_str})"
        )
        verdict_rows.append(("FP event count", f"S5/{appr}", value_str, v))
        if v == "FAIL":
            fails.append(f"FP event count ({appr}): {int(n_events)} events (gate = 0)")

    # ── SNR bias per appraiser vs truth ──────────────────────────────────────
    for appr, info in bias_results.items():
        b = info.get("mean_bias", float("nan"))
        if math.isnan(b):
            continue
        v = _verdict_bias(b)
        verdict_rows.append(("SNR bias vs truth", f"S1/{appr}", f"{b:+.2f} dB", v))
        if v == "FAIL":
            fails.append(f"SNR bias ({appr}) = {b:+.2f} dB (gate <= {THRESH_BIAS_PASS} dB)")

    # ── McNemar — all pairs ──────────────────────────────────────────────────
    for (a, b), mc in mcnemar.items():
        p_mc = mc.get("p_value", float("nan"))
        if math.isnan(p_mc):
            continue
        v = mc.get("verdict", "—")
        verdict_rows.append((f"McNemar chi2 p-value ({a} vs {b})", "all",
                             f"p={p_mc:.4f}", v))
        if v == "FAIL":
            fails.append(
                f"McNemar test ({a} vs {b}) rejected at p={p_mc:.4f} "
                f"(alpha={THRESH_MCNEMAR_P})"
            )

    # ── Fisher FP parity — all pairs ─────────────────────────────────────────
    for (a, b), fish in fisher_fp.items():
        p_fisher = fish.get("p_value", float("nan"))
        if math.isnan(p_fisher):
            continue
        v = fish.get("verdict", "—")
        verdict_rows.append((f"FP parity Fisher p ({a} vs {b})", "S5",
                             f"p={p_fisher:.4f}", v))
        if v == "FAIL":
            fails.append(
                f"FP parity Fisher ({a} vs {b}) rejected at p={p_fisher:.4f}"
            )

    overall = "FAIL" if fails else ("MARGINAL" if marginals else "PASS")
    return verdict_rows, overall, fails


