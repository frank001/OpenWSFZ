_TREND_COLUMNS_XPLAT = [
    "run_date", "git_sha",
    "pct_grr_snr", "ndc_snr",
    "platform_snr_bias_db",      # bias: Linux − Windows (backward compat)
    "win_wsjt_snr_bias_db",      # bias: WSJT-X − Windows
    "linux_wsjt_snr_bias_db",    # bias: WSJT-X − Linux
    "between_platform_kappa",
    "fp_events_windows", "fp_events_linux", "fp_events_wsjt",
    "mcnemar_p",                 # Windows vs Linux McNemar p-value
    "overall",
]


def _append_trend(
    qa_rr_root: Path,
    run_dir: Path,
    git_sha: str,
    continuous_results: dict,
    kappa_results: dict,
    fp_results: dict,
    platform_bias: dict,
    mcnemar: dict,
    overall: str,
) -> None:
    import csv as _csv
    trend_path = qa_rr_root / "trend_xplat.csv"
    write_header = not trend_path.exists()

    run_date_parts = run_dir.name.split("-")[0:3]
    run_date_str   = "-".join(run_date_parts) if len(run_date_parts) >= 3 else run_dir.name

    def _safe(v: Any) -> str:
        if v is None or (isinstance(v, float) and math.isnan(v)):
            return ""
        return str(round(v, 4) if isinstance(v, float) else v)

    pct_grr_snr = ndc_snr = ""
    if "S1" in continuous_results and continuous_results["S1"] is not None:
        pct_grr_snr = _safe(continuous_results["S1"]["metrics"]["pct_contribution_grr"])
        ndc_snr     = _safe(continuous_results["S1"]["metrics"]["ndc"])

    # S1 pairwise biases
    s1_bias = platform_bias.get("S1", {})
    platform_snr_bias_db    = _safe(s1_bias.get(("Windows", "Linux"),  {}).get("mean_bias", float("nan")))
    win_wsjt_snr_bias_db    = _safe(s1_bias.get(("Windows", "WSJT-X"), {}).get("mean_bias", float("nan")))
    linux_wsjt_snr_bias_db  = _safe(s1_bias.get(("Linux",   "WSJT-X"), {}).get("mean_bias", float("nan")))

    # Between-appraiser kappa (prefer Windows vs Linux for backward compat)
    bk_label = "Windows_vs_Linux"
    bk = kappa_results.get(bk_label, kappa_results.get("between_platforms", {}))
    between_kappa = _safe(bk.get("kappa", float("nan")))

    win_fp   = _safe(fp_results.get("Windows", {}).get("n_fp_events", float("nan")))
    lin_fp   = _safe(fp_results.get("Linux",   {}).get("n_fp_events", float("nan")))
    wsjt_fp  = _safe(fp_results.get("WSJT-X",  {}).get("n_fp_events", float("nan")))

    # McNemar p for Windows vs Linux pair (backward compat)
    mc_p = _safe(mcnemar.get(("Windows", "Linux"), {}).get("p_value", float("nan")))

    row = {
        "run_date":             run_date_str,
        "git_sha":              git_sha,
        "pct_grr_snr":          pct_grr_snr,
        "ndc_snr":              ndc_snr,
        "platform_snr_bias_db": platform_snr_bias_db,
        "win_wsjt_snr_bias_db": win_wsjt_snr_bias_db,
        "linux_wsjt_snr_bias_db": linux_wsjt_snr_bias_db,
        "between_platform_kappa": between_kappa,
        "fp_events_windows":    win_fp,
        "fp_events_linux":      lin_fp,
        "fp_events_wsjt":       wsjt_fp,
        "mcnemar_p":            mc_p,
        "overall":              overall,
    }

    with open(trend_path, "a", newline="", encoding="utf-8") as fh:
        writer = _csv.DictWriter(fh, fieldnames=_TREND_COLUMNS_XPLAT)
        if write_header:
            writer.writeheader()
        writer.writerow(row)


