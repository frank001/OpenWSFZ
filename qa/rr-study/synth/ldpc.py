def encode_ldpc(message_plus_crc: list[int]) -> list[int]:
    """Return the 174-bit systematic codeword: 91 info bits followed by 83 parity bits.

    The parity of bit i is the GF(2) dot product of the i-th generator row with
    the 91 info bits.

    Source for the generator matrix: published FT8 LDPC(174,91) specification,
    Franke/Somerville/Taylor, QEX July/August 2020.

    Args:
        message_plus_crc: list of exactly 91 bits (77 message + 14 CRC), MSB first.

    Returns:
        list of exactly 174 bits: info bits[0..90] ++ parity bits[0..82], MSB first.

    Raises:
        ValueError: if input length ≠ 91.
    """
    if len(message_plus_crc) != MESSAGE_PLUS_CRC_BITS:
        raise ValueError(
            f"expected {MESSAGE_PLUS_CRC_BITS} message+CRC bits, got {len(message_plus_crc)}"
        )

    parity: list[int] = []
    for row in _LDPC_GENERATOR:
        bit = 0
        for j in range(MESSAGE_PLUS_CRC_BITS):   # 91 bits only; padding bits are 0
            if (row[j >> 3] >> (7 - (j & 7))) & 1:
                bit ^= message_plus_crc[j]
        parity.append(bit)

    return list(message_plus_crc) + parity


