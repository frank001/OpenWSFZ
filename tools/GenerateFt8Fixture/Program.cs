/// <summary>
/// One-shot generator: produces tests/OpenWSFZ.Ft8.Tests/Fixtures/ft8-sample.wav
/// and ft8-sample.ref, which are committed as EmbeddedResources.
///
/// The fixture is a 15-second 12 kHz mono IEEE float-32 WAV file containing the
/// synthetic FT8 frame for "CQ Q1AW FN31" at 1500 Hz base frequency, startSample=0.
///
/// Run once:  dotnet run --project tools/GenerateFt8Fixture
/// </summary>

Ft8FixtureGenerator.Run(args);

internal static class Ft8FixtureGenerator
{
    public static void Run(string[] args)
    {
        var repoRoot = FindRepoRoot(AppContext.BaseDirectory)
            ?? throw new InvalidOperationException("Could not find repo root (no .git directory found)");

        var fixturesDir = Path.Combine(repoRoot, "tests", "OpenWSFZ.Ft8.Tests", "Fixtures");
        Directory.CreateDirectory(fixturesDir);

        // ── Generate FT8 PCM ─────────────────────────────────────────────────
        const double baseFreqHz = 1500.0;
        ulong c1 = 2;           // "CQ"
        ulong c2 = 1_674_730;   // "Q1AW" (pre-computed 28-bit value)
        ulong rg = 10_331;      // "FN31" (pre-computed 15-bit value)

        byte[] msgBits  = PackType1(c1, c2, rg);
        byte[] infoBits = AppendCrc14(msgBits);
        byte[] codeword = LdpcEncode(infoBits);
        int[]  symbols  = BitsToSymbols(codeword);
        float[] pcm     = SymbolsToPcm(symbols, baseFreqHz, startSample: 0);

        // ── Write WAV ────────────────────────────────────────────────────────
        var wavPath = Path.Combine(fixturesDir, "ft8-sample.wav");
        WriteIeeeFloatWav(wavPath, pcm, sampleRate: 12_000, channels: 1);
        Console.WriteLine($"Written: {wavPath}  ({new FileInfo(wavPath).Length:N0} bytes)");

        // ── Write reference decode file ───────────────────────────────────────
        var refPath = Path.Combine(fixturesDir, "ft8-sample.ref");
        File.WriteAllText(refPath, "CQ Q1AW FN31\n");
        Console.WriteLine($"Written: {refPath}");

        Console.WriteLine("Done. Commit both files and add EmbeddedResource entries to the test project.");
    }

    // ─────────────────────────────────────────────────────────────────────────
    // Helpers
    // ─────────────────────────────────────────────────────────────────────────

    private static string? FindRepoRoot(string startDir)
    {
        var dir = new DirectoryInfo(startDir);
        while (dir != null)
        {
            if (Directory.Exists(Path.Combine(dir.FullName, ".git"))) return dir.FullName;
            dir = dir.Parent;
        }
        return null;
    }

    private static void WriteIeeeFloatWav(string path, float[] samples, int sampleRate, int channels)
    {
        const int bitsPerSample = 32;
        int byteRate   = sampleRate * channels * bitsPerSample / 8;
        int blockAlign = channels * bitsPerSample / 8;
        int dataSize   = samples.Length * 4;
        int chunkSize  = 36 + dataSize;

        using var fs = new FileStream(path, FileMode.Create, FileAccess.Write);
        using var w  = new BinaryWriter(fs);

        // RIFF header
        w.Write((byte)'R'); w.Write((byte)'I'); w.Write((byte)'F'); w.Write((byte)'F');
        w.Write(chunkSize);
        w.Write((byte)'W'); w.Write((byte)'A'); w.Write((byte)'V'); w.Write((byte)'E');

        // fmt chunk
        w.Write((byte)'f'); w.Write((byte)'m'); w.Write((byte)'t'); w.Write((byte)' ');
        w.Write(16);                    // chunk size
        w.Write((ushort)3);             // PCM_IEEE_FLOAT
        w.Write((ushort)channels);
        w.Write(sampleRate);
        w.Write(byteRate);
        w.Write((ushort)blockAlign);
        w.Write((ushort)bitsPerSample);

        // data chunk
        w.Write((byte)'d'); w.Write((byte)'a'); w.Write((byte)'t'); w.Write((byte)'a');
        w.Write(dataSize);

        foreach (var s in samples)
            w.Write(s);
    }

    // ── FT8 encoding (mirrors TestFt8Encoder in the test project) ─────────────

    private static byte[] PackType1(ulong c1, ulong c2, ulong rg, int n3 = 0, int i3 = 0)
    {
        var bits = new byte[77];
        PackBitsInto(bits,  0, 28, c1);
        PackBitsInto(bits, 28, 28, c2);
        PackBitsInto(bits, 56, 15, rg);
        PackBitsInto(bits, 71,  3, (ulong)n3);
        PackBitsInto(bits, 74,  3, (ulong)i3);
        return bits;
    }

    private static byte[] AppendCrc14(byte[] msgBits)
    {
        var info = new byte[91];
        Array.Copy(msgBits, info, 77);

        var crcBuf = new byte[82];
        Array.Copy(msgBits, crcBuf, 77);
        uint crc = Crc14Compute(crcBuf, 82);

        for (int i = 0; i < 14; i++)
            info[77 + i] = (byte)((crc >> (13 - i)) & 1);
        return info;
    }

    private static uint Crc14Compute(byte[] bits, int length)
    {
        // Must match Crc14.Compute in OpenWSFZ.Ft8/Dsp/Crc14.cs exactly.
        // Franke & Taylor 2019 / WSJT-X CRC-14: feedback-XOR style.
        const uint poly = 0x2757u;
        const int  crcBits = 14;
        const uint mask    = (1u << crcBits) - 1u; // 0x3FFF

        uint crc = 0u;
        for (int i = 0; i < length; i++)
        {
            uint bit      = bits[i] & 1u;
            uint feedback = ((crc >> (crcBits - 1)) ^ bit) & 1u;
            crc           = (crc << 1) & mask;
            if (feedback != 0)
                crc ^= poly;
        }
        return crc;
    }

    private static byte[] LdpcEncode(byte[] infoBits)
    {
        var codeword = new byte[174];
        Array.Copy(infoBits, codeword, 91);

        for (int p = 0; p < 83; p++)
        {
            byte parity = 0;
            for (int b = 0; b < 91; b++)
            {
                if (((s_ldpcGen[p][b >> 3] >> (7 - (b & 7))) & 1) == 1)
                    parity ^= infoBits[b];
            }
            codeword[91 + p] = parity;
        }
        return codeword;
    }

    private static int[] BitsToSymbols(byte[] codeword)
    {
        const int symbolCount = 79;
        var symbols = new int[symbolCount];

        ReadOnlySpan<int> costas    = [3, 1, 4, 0, 6, 5, 2];
        ReadOnlySpan<int> costasPos = [0, 36, 72];
        foreach (int pos in costasPos)
            for (int i = 0; i < 7; i++)
                symbols[pos + i] = costas[i];

        var dataPos = new List<int>(58);
        for (int s = 0; s < symbolCount; s++)
        {
            bool isCostas = s < 7 || (s >= 36 && s < 43) || (s >= 72 && s < 79);
            if (!isCostas) dataPos.Add(s);
        }

        ReadOnlySpan<int> grayMap = [0, 1, 3, 2, 5, 6, 4, 7];
        for (int di = 0; di < 58; di++)
        {
            int binary = (codeword[di * 3] << 2) | (codeword[di * 3 + 1] << 1) | codeword[di * 3 + 2];
            symbols[dataPos[di]] = grayMap[binary];
        }
        return symbols;
    }

    private static float[] SymbolsToPcm(int[] symbols, double baseFreqHz, int startSample = 0, float amplitude = 0.5f)
    {
        const int sampleRate       = 12_000;
        const int samplesPerSymbol = 1_920;
        const double toneSpacing   = 6.25;
        const int totalSamples     = 180_000;

        var pcm = new float[totalSamples];
        for (int sym = 0; sym < symbols.Length; sym++)
        {
            double toneHz = baseFreqHz + symbols[sym] * toneSpacing;
            int    offset = startSample + sym * samplesPerSymbol;
            for (int s = 0; s < samplesPerSymbol; s++)
            {
                int idx = offset + s;
                if ((uint)idx < (uint)totalSamples)
                    pcm[idx] = amplitude * (float)Math.Sin(2.0 * Math.PI * toneHz * s / sampleRate);
            }
        }
        return pcm;
    }

    private static void PackBitsInto(byte[] bits, int start, int count, ulong value)
    {
        for (int i = count - 1; i >= 0; i--)
        {
            bits[start + i] = (byte)(value & 1);
            value >>= 1;
        }
    }

