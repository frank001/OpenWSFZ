    /// <summary>
    /// Packs a Type 1 (i3=0, n3=0) standard QSO message into 77 bits.
    /// Use <see cref="EncodeCallsign28"/> and <see cref="EncodeReport15Grid"/> to
    /// convert human-readable values to the packed field integers.
    /// </summary>
    /// <param name="c1">28-bit packed callsign 1 (e.g. 2 for "CQ").</param>
    /// <param name="c2">28-bit packed callsign 2.</param>
    /// <param name="rg">15-bit packed report/grid.</param>
    /// <param name="n3">3-bit sub-type indicator (0 for standard grid/report).</param>
    /// <param name="i3">3-bit message type (0 for standard Type 1).</param>
    public static byte[] PackType1(ulong c1, ulong c2, ulong rg, int n3 = 0, int i3 = 0)
    {
        var bits = new byte[77];
        PackBitsInto(bits,  0, 28, c1);
        PackBitsInto(bits, 28, 28, c2);
        PackBitsInto(bits, 56, 15, rg);
        PackBitsInto(bits, 71,  3, (ulong)n3);
        PackBitsInto(bits, 74,  3, (ulong)i3);
        return bits;
    }

    /// <summary>
    /// Encodes a callsign string into a 28-bit packed integer using the FT8 standard
    /// mixed-radix encoding (Franke &amp; Taylor 2019; kgoba/ft8_lib pack.c).
    ///
    /// The district digit (first decimal digit) is placed at position 2 of the
    /// 6-character form; positions 3-5 hold the suffix (letter/space only);
    /// positions 0-1 hold the prefix (letter, digit, or space).
    /// Special values: 0="DE", 1="QRZ", 2="CQ".
    /// </summary>
    public static ulong EncodeCallsign28(string callsign)
    {
        if (callsign.Equals("DE",  StringComparison.OrdinalIgnoreCase)) return 0;
        if (callsign.Equals("QRZ", StringComparison.OrdinalIgnoreCase)) return 1;
        if (callsign.Equals("CQ",  StringComparison.OrdinalIgnoreCase)) return 2;

        string cs = callsign.ToUpperInvariant();

        // Find the DISTRICT digit — the LAST decimal digit in the callsign
        // (matching ft8_lib's pack.c convention).
        // The district digit is ALWAYS the last digit before the letter-only suffix:
        //   "9A7W"  → last digit = '7' at idx 2 (prefix="9A", suffix="W")
        //   "K0ABC" → last digit = '0' at idx 1 (prefix="K", suffix="ABC")
        //   "LW9END"→ last digit = '9' at idx 2 (prefix="LW", suffix="END")
        int digitPos = -1;
        for (int i = cs.Length - 1; i >= 0; i--)
            if (char.IsDigit(cs[i])) { digitPos = i; break; }

        if (digitPos < 0 || digitPos > 2)
        {
            // Fallback: left-pad and place as-is (handles edge cases).
            digitPos = Math.Min(2, cs.Length - 1);
        }

        // Build the 6-character form: prefix right-aligned before position 2,
        // district digit at position 2, suffix at positions 3-5, padded with spaces.
        char[] c6 = new char[6] { ' ', ' ', ' ', ' ', ' ', ' ' };
        for (int i = 0; i < digitPos && 2 - digitPos + i >= 0; i++)
            c6[2 - digitPos + i] = cs[i];
        c6[2] = cs[digitPos];
        for (int i = 0; i < 3 && digitPos + 1 + i < cs.Length; i++)
            c6[3 + i] = cs[digitPos + 1 + i];

        // Mixed-radix encode:
        //   pos 0,1 : from {space, 0-9, A-Z} (37 values; space=0, '0'=1…'9'=10, A=11…Z=36)
        //   pos 2   : from {0-9}             (10 values)
        //   pos 3-5 : from {space, A-Z}      (27 values; space=0, A=1…Z=26)
        static ulong idx37(char ch)
            => ch == ' ' ? 0UL : (ch >= '0' && ch <= '9') ? (ulong)(ch - '0' + 1) : (ulong)(ch - 'A' + 11);
        static ulong idx10(char ch) => (ulong)(ch - '0');
        static ulong idx27(char ch) => ch == ' ' ? 0UL : (ulong)(ch - 'A' + 1);

        ulong n = idx37(c6[0]);
        n = n * 37 + idx37(c6[1]);
        n = n * 10 + idx10(c6[2]);
        n = n * 27 + idx27(c6[3]);
        n = n * 27 + idx27(c6[4]);
        n = n * 27 + idx27(c6[5]);

        return n + 3;
    }

    /// <summary>
    /// Encodes a 4-character Maidenhead grid square (e.g. "FN31") as a 15-bit packed integer.
    /// </summary>
    public static ulong EncodeReport15Grid(string grid)
    {
        const string letters = "ABCDEFGHIJKLMNOPQR";
        if (grid.Length < 4) return 0;
        int r1 = letters.IndexOf(char.ToUpper(grid[0]));
        int r2 = letters.IndexOf(char.ToUpper(grid[1]));
        int r3 = grid[2] - '0';
        int r4 = grid[3] - '0';
        if (r1 < 0 || r2 < 0 || r3 < 0 || r4 < 0) return 0;
        return (ulong)(r1 * 1800 + r2 * 100 + r3 * 10 + r4);
    }

    /// <summary>
    /// Appends a 14-bit CRC to the 77 message bits, returning a 91-bit information vector.
    ///
    /// <para>
    /// Matches the FT8 standard (kgoba/ft8_lib <c>crc.c ftx_add_crc()</c>): the CRC is
    /// computed over <b>82 bits</b> — the 77 message bits followed by 5 implicit zero-padding
    /// bits — and stored MSB-first at bit positions [77..90] of the returned array.
    /// </para>
    /// </summary>
    public static byte[] AppendCrc14(byte[] msgBits)
    {
        var info = new byte[91];
        Array.Copy(msgBits, info, 77);

        // FT8 CRC is computed over 82 bits: 77 message bits + 5 zero-padding bits.
        // See kgoba/ft8_lib crc.c ftx_add_crc(): ftx_compute_crc(a91, 96−14) = 82 bits.
        var crcBuf = new byte[82]; // zero-initialised; crcBuf[77..81] = 0
        Array.Copy(msgBits, crcBuf, 77);
        uint crc = Crc14.Compute(crcBuf, 82);

        for (int i = 0; i < 14; i++)
            info[77 + i] = (byte)((crc >> (13 - i)) & 1);
        return info;
    }

    /// <summary>
    /// LDPC-encodes 91 information bits to a 174-bit codeword using the
    /// pre-computed FT8 (174,91) generator matrix from kgoba/ft8_lib.
    ///
    /// The 91 systematic bits (info) occupy codeword positions 0–90; the 83
    /// computed parity bits occupy positions 91–173.
    ///
    /// Each row of <see cref="LdpcGenerator"/> is a 12-byte (96-bit) MSB-first
    /// bitmask over the 91 info bits that XOR to produce one parity bit.
    /// </summary>
    public static byte[] LdpcEncode(byte[] infoBits)
    {
        var codeword = new byte[174];
        Array.Copy(infoBits, codeword, 91); // systematic part

        for (int p = 0; p < 83; p++)
        {
            byte parity = 0;
            for (int b = 0; b < 91; b++)
            {
                // Row p, bit b: MSB-first within each byte.
                if (((LdpcGenerator[p][b >> 3] >> (7 - (b & 7))) & 1) == 1)
                    parity ^= infoBits[b];
            }
            codeword[91 + p] = parity;
        }

        return codeword;
    }

